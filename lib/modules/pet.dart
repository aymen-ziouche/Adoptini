import 'dart:ui';

class Pet {
  final String name;
  final String breed;
  final String age;
  final String image;
  final String description;
  final String ownerId;
  
  Pet({
    required this.name,
    required this.breed,
    required this.age,
    required this.image,
    required this.description,
    required this.ownerId,
  });
}